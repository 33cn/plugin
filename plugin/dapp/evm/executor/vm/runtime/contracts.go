// PrecompiledContract 系统内置合约实现的接口，只包含两个操作：
// 1 根据合约自身逻辑和入参，计算所需Gas；
// 2 执行合约。
type PrecompiledContract interface {
	// 计算当前合约执行需要消耗的Gas
	RequiredGas(input []byte) uint64

	// 执行预编译的合约固定逻辑，input为入参
	Run(input []byte) ([]byte, error)
}

